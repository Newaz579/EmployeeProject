// Base Employee class
class Employee {
    constructor(id, name, age, gender, birthdate, email, contactNo, emergencyContactNo, bloodGroup, presentAddress, permanentAddress, department) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.birthdate = birthdate;
        this.email = email;
        this.contactNo = contactNo;
        this.emergencyContactNo = emergencyContactNo;
        this.bloodGroup = bloodGroup;
        this.presentAddress = presentAddress;
        this.permanentAddress = permanentAddress;
        this.department = department;
    }
}
